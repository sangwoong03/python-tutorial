# List in python
about_me = ["sangwoong", 27, "Osan"]

print("================================ List ================================")
print(about_me)
print(about_me[0])
about_me[1] = 17
print(about_me[1])
print(about_me[2])

# Dictionary
about_me_detail = {"name": "sangwoong", "age": 27, "where": "Osan"}
print("================================ Dictionary ================================")
print(about_me_detail)
print(about_me_detail["name"])
about_me_detail["age"] = 17
print(about_me_detail["age"])
print(about_me_detail["where"])
